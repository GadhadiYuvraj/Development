# 1️⃣ Arithmetic Operators
# An arithmetic operator in Python is used to perform mathematical calculations such as addition, subtraction, multiplication, division, exponentiation, and remainder operations on numeric operands.
# Used for mathematical calculations.

# | Operator | Meaning             | Example   | Result |
# | -------- | ------------------- | --------- | ------ |
# | `+`      | Addition            | `5 + 3`   | `8`    |
# | `-`      | Subtraction         | `5 - 3`   | `2`    |
# | `*`      | Multiplication      | `5 * 3`   | `15`   |
# | `/`      | Division            | `10 / 2`  | `5.0`  |
# | `//`     | Floor Division      | `10 // 3` | `3`    |
# | `%`      | Modulus (remainder) | `10 % 3`  | `1`    |
# | `**`     | Exponent (power)    | `2 ** 3`  | `8`    |


